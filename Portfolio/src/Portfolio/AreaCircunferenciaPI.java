package Portfolio;

public class AreaCircunferenciaPI {

	public static void main(String[] args) {
		
		double raio = 3.5;
		double PI = 3.1415;
		
		double area = raio * PI * raio;
		
		System.out.println(area);
	
			
		raio = 10;
		area = raio * PI * raio;
		
		System.out.println(area);
	
	}
	
	
	
}
